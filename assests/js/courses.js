let swiper;

function renderCourses(category = 'popular') {
  const courseListElement = document.getElementById(`course-list-${category.replace(/_/g, '-')}`);

  if (!courseListElement) return;

  // Clear the existing slides to prevent duplication
  courseListElement.innerHTML = '';

  // Loop through the courses for the selected category
  if (courseData[category]) {
    courseData[category].forEach(course => {
      const slide = document.createElement('div');
      slide.classList.add('col-md-12','col-12','col-sm-12','swiper-slide');
      slide.innerHTML = `
        <div class="program-card gl-card">
          <div class="program-card-link module-card">
            <div class="program-card__banner">
              <img src="${course.image}" height="168" class="lazy program-image gl-lazy-loaded fadeIn"
                  alt="${course.title}" title="${course.title}">
              <img src="${course.logo}" width="110" height="72" class="partner-logo lazy gl-lazy-loaded fadeIn"
                  alt="${course.partner}" title="${course.partner}">
              <div class="promoted-badge bg-danger">Promoted</div>
            </div>
            <div class="program-card__body program-detail">
              <div class="program-card__head">
                <p class="program-detail__partner">${course.partner}</p>
                <a href="${course.link}" class="track-click-mp module-card__link"
                   data-title="Program Cards_Popular"
                   data-event-type="Learn more_${course.title}" aria-label="${course.title}">
                  <h3 class="program-detail__name">${course.title}</h3>
                </a>
              </div>
              <div class="program-card__foot">
                <p class="program-detail__term">${course.duration} · ${course.mode}</p>
                <div class="program-detail__badge-wrapper">
                  <div class="program-detail__badge">${course.badge}</div>
                </div>
              </div>
            </div>
          </div>
          <a href="${course.link}" class="track-click-mp program-card__button"
             data-title="Program Cards_Popular" data-event-type="View Program Popular">
            <p class="program-detail__cta">View Program</p>
          </a>
        </div>`;
      courseListElement.appendChild(slide);
    });
  }

  // Initialize or update the Swiper instance
  if (swiper) swiper.destroy(); // Destroy previous swiper instance if it exists

  swiper = new Swiper('.swiper-container', {
    slidesPerView: 1,
    centeredSlides: true, 
    spaceBetween: 10,
    navigation: {
      nextEl: '.swiper-button-next',
      prevEl: '.swiper-button-prev',
    },
    pagination: {
      el: '.swiper-pagination',
      clickable: true,
      renderBullet: function (index, className) {
        const totalSlides = this.slides.length; 
        console.log(totalSlides);// Use `this` to reference swiper instance
        const visibleBullets = Math.max(0, totalSlides - 2); // Reduce the number of bullets by 1
        return index < visibleBullets ? `<span class="${className}"></span>` : ''; // Only render up to the limit
      },
      
    },
    breakpoints: {
      576: {
        slidesPerView: 1, // One card for screens <= 576px
        centeredSlides: true,
      },
      768: {
        slidesPerView: 2, // Two cards for screens >= 768px
        centeredSlides: true,
      },
      992: {
        slidesPerView: 3, // Optional: Three cards for larger screens
      },
    },
    on: {
      init: function () {
        toggleNavigationButtons(this);
        checkSlidesCount(this);
      },
      slideChange: function () {
        toggleNavigationButtons(this);
        checkSlidesCount(this);
      },
    },
  });

  function toggleNavigationButtons(swiper) {
    swiper.navigation.nextEl.classList.toggle(
      'swiper-button-disabled',
      swiper.isEnd
    );
    swiper.navigation.prevEl.classList.toggle(
      'swiper-button-disabled',
      swiper.isBeginning
    );
  }

  
function checkSlidesCount(swiper) {
  const totalSlides = swiper.slides.length;
  const slidesPerView = Math.floor(swiper.params.slidesPerView); // Ensure integer
  const currentIndex = swiper.activeIndex;

  const isEnd = currentIndex >= totalSlides - slidesPerView-1;
  console.log(`isEnd: ${isEnd}, activeIndex: ${currentIndex}`);

  // Disable Next button when the last set of slides is visible
  swiper.navigation.nextEl.classList.toggle('swiper-button-disabled', isEnd);
  swiper.navigation.prevEl.classList.toggle('swiper-button-disabled', swiper.isBeginning);
}
  
}

// Event listener for category clicks
document.querySelectorAll('.js-program-head').forEach(item => {
  item.addEventListener('click', event => {
    const category = item.getAttribute('data-rel');
    const cardBlock = document.getElementById(category);

    if (cardBlock) {
      // Remove 'active' class from all blocks
      document.querySelectorAll('.program-card-block').forEach(block => {
        block.classList.remove('active');
      });
      // Add 'active' class to the clicked block
      cardBlock.classList.add('active');
      renderCourses(category); // Render courses for the selected category
    }
  });
});

